import { Uri, Webview } from "vscode";

//https://blog.csdn.net/M_Jin/article/details/104865016
const urlRegex = /^(?:(http|https|ftp):\/\/)?((?:[\w-]+\.)+[a-z0-9]+)((?:\/[^/?#]*)+)?(\?[^#]+)?(#.+)?$/i;
//https://blog.51cto.com/u_15527951/6778989
const pathRegex = /^(([a-zA-Z]:|\.\.?|)[\\\/]{1,2})(?:[^\\\/:*?"<>|\r\n]+[\/\\])*[^\\\/:*?"<>|\r\n]*$/;
export function checkUrl(url: string): boolean{
    return urlRegex.test(url) || pathRegex.test(url);
}

